					Piyush received his bachelors degree in Computer Science from highly reputed Indian Institute of Technology, Roorkee in August 2003.
					He received his doctorate from University of Florida in August 2010. After graduating he moved to France where he worked as researcher at INRIA
					Bretagne-Atlantique center in Rennes, until April 2013. At INRIA he was involved in a multi-million Euro European Union project where the goal was to develop a
					federation of clouds supporting extensive SLAs and QoP constraint providing users fine grained control over location of their compute resources 
					and data. Since April 2013, he has joined Zurich University of Applied Sciences as a tenure-track researcher. Other than cloud computing research, his research interests are presently also in 
